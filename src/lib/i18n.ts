// Bilingual support. English is the default and lives at the site root; Thai
// lives under /th/. A lesson's language is encoded in its directory: Thai
// files live under `lessons/th/...`, so their content id starts with `th/`.
// (A directory prefix survives Astro's id slugification, where a `.th`
// filename suffix would not.) The "key" — the id with any `th/` prefix
// stripped — is shared across languages, so links and reading progress stay
// paired between the two editions.

import type { CollectionEntry } from 'astro:content';

export const LANGS = ['en', 'th'] as const;
export type Lang = (typeof LANGS)[number];
export const DEFAULT_LANG: Lang = 'en';

export function langFromId(id: string): Lang {
  return id.startsWith('th/') ? 'th' : 'en';
}

export function keyFromId(id: string): string {
  return id.replace(/^th\//, '');
}

export function lessonLang(entry: CollectionEntry<'lessons'>): Lang {
  return entry.data.lang;
}

/** Localized URL helpers — Thai paths get a /th prefix. */
export function homeHref(lang: Lang): string {
  return lang === 'en' ? '/' : '/th/';
}
export function lessonHref(lang: Lang, key: string): string {
  return lang === 'en' ? `/lessons/${key}/` : `/th/lessons/${key}/`;
}
export function trackHref(lang: Lang, id: string): string {
  return lang === 'en' ? `/tracks/${id}/` : `/th/tracks/${id}/`;
}
export function aboutHref(lang: Lang): string {
  return lang === 'en' ? '/about/' : '/th/about/';
}
export function searchHref(lang: Lang): string {
  return lang === 'en' ? '/search/' : '/th/search/';
}

/** "11 min" → Thai "11 นาที" for the read-time label. */
export function localizeDuration(duration: string, lang: Lang): string {
  return lang === 'th' ? duration.replace(/min/i, 'นาที') : duration;
}

