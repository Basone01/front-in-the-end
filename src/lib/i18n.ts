/** The Thai read-time label puts the word first: "อ่าน 11 นาที" vs "11 min read". */
export function readLabel(duration: string, lang: Lang): string {
  return lang === 'th'
    ? `${STR.th.readSuffix} ${localizeDuration(duration, 'th')}`
    : `${duration} ${STR.en.readSuffix}`;
}
